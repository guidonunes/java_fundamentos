package com.java.operators;

public class Operators {
	public static void main(String[] args) {
		int x = 5;
		int y = 10;

		
		int result = x + y;
		System.out.println(result);
		
		result = x - y;
		System.out.println(result);
		
		result = x * y;
		System.out.println(result);
		
		result = x / y;
		System.out.println(result);
		
		result = x % y;
		System.out.println(result);
	}
}
